package clases;

public class MantPreventivo extends Mantenimiento {
    
    private int numero_frecuencia;
    private String unidad_frecuencia;
    
    public int getNumero_frecuencia() {
        return numero_frecuencia;
    }

    public void setNumero_frecuencia(int numero_frecuencia) {
        this.numero_frecuencia = numero_frecuencia;
    }

    public String getUnidad_frecuencia() {
        return unidad_frecuencia;
    }

    public void setUnidad_frecuencia(String unidad_frecuencia) {
        this.unidad_frecuencia = unidad_frecuencia;
    }

    
    
}
