    private void tab_maquinariasMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tab_maquinariasMousePressed
        if (evt.getClickCount() == 2) {
            
            int fila = tab_maquinarias.getSelectedRow();
            if (destino.equals("MP")) {//Si lo llama la ventana de mantenimientos preventivos
                frm_mant_prev.txt_id_maquinaria.setText(tab_maquinarias.getValueAt(fila, 0).toString());
                frm_mant_prev.txt_codigo_maquinaria.setText(tab_maquinarias.getValueAt(fila, 3).toString());
            } else if (destino.equals("MC")) {//Si lo llama la ventana de mantenimientos correctivos
                frm_mant_corr.txt_id_maquinaria.setText(tab_maquinarias.getValueAt(fila, 0).toString());
                frm_mant_corr.txt_codigo_maquinaria.setText(tab_maquinarias.getValueAt(fila, 3).toString());
            }
            
            this.dispose();
        }
    }//GEN-LAST:event_tab_maquinariasMousePressed

