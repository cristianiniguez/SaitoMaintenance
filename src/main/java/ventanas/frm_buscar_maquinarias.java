    //Muestra las maquinarias segun el area
    public static void MostrarMaquinarias() {
        tab_maquinarias.setModel(new ConsulMaquinaria().mostrar(cmb_areas.getSelectedItem().toString()));
        OcultarColumnasMaquinaria();
        lbl_num_registros_maquinaria.setText("Número de maquinarias: " + ConsulMaquinaria.num_registros);
    }

    private static void OcultarColumnasMaquinaria() {
        int[] columnas = {0, 1};
        for (int columna : columnas) {
            tab_maquinarias.getColumnModel().getColumn(columna).setMaxWidth(0);
            tab_maquinarias.getColumnModel().getColumn(columna).setMinWidth(0);
            tab_maquinarias.getColumnModel().getColumn(columna).setPreferredWidth(0);
        }
    }

