    private void btn_guardar_dias_laboralesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardar_dias_laboralesActionPerformed
        //Lectura de datos
        boolean[] dts = new boolean[7];
        dts[0] = chb_domingo.isSelected();
        dts[1] = chb_lunes.isSelected();
        dts[2] = chb_martes.isSelected();
        dts[3] = chb_miercoles.isSelected();
        dts[4] = chb_jueves.isSelected();
        dts[5] = chb_viernes.isSelected();
        dts[6] = chb_sabado.isSelected();
        
        //Verificando si hay agendas registradas
        if (new ConsulAgenda().conflictosDiasLaborales()) {
            String msg = "Se han registrado agendas hata ahora\n"
                    + "Para cambiar los dias laborales elimine todas las agendas primero\n"
                    + "Recuerde que puede exportar la informacion de las agendas a PDF o Excel antes de eliminarlas";
            JOptionPane.showMessageDialog(this, msg);
            return;
        }

        //Envio de datos
        ConsulDiasLaborales func = new ConsulDiasLaborales();
        if (func.actualizar(dts)) {
            JOptionPane.showMessageDialog(this, "Días laborales actualizados satisfactoriamente");
        }
    }//GEN-LAST:event_btn_guardar_dias_laboralesActionPerformed

