    private void btn_guardar_feriadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardar_feriadoActionPerformed
        //Validacion de datos
        if (txt_descripcion_feriado.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Se debe ingresar una descripción para el feriado");
            txt_descripcion_feriado.requestFocus();
            return;
        }

        //Lectura de datos
        Feriado dts = new Feriado();
        dts.setDescripcion(txt_descripcion_feriado.getText());

        //Envio de datos
        ConsulFeriado func = new ConsulFeriado();
        if (accionFeriado.equals("guardar")) {
            Calendar cal1 = dtc_fecha_feriado1.getCalendar();
            Calendar cal2;
            //¿El feriado consiste de varios dias seguidos?
            if (chb_variosDias.isSelected()) {
                cal2 = dtc_fecha_feriado2.getCalendar();
            } else {
                cal2 = cal1;
            }
            //Verificando conflictos
            for (Date fecha : Auxiliar.SQLDateRange(cal1, cal2)) {
                //Verificando que no hay conflictos con agendas
                if (new ConsulAgenda().conflictosFeriado(fecha)) {
                    JOptionPane.showMessageDialog(this, "Existen agendas que causan conflictos");
                    return;
                }
                //Verificando que no hay un feriado ya registrado
                if(new ConsulFeriado().verificar(fecha)){
                    JOptionPane.showMessageDialog(this, "Uno de los feriados ya esta registrado");
                    return;
                }
            }
            //Enviando los feriados
            boolean enviados = true;
            for (Date fecha : Auxiliar.SQLDateRange(cal1, cal2)) {
                dts.setFecha(fecha);
                System.out.println("Enviando " + fecha);
                if (!new ConsulFeriado().insertar(dts)) {
                    System.out.println("No se envio");
                    enviados = false;
                    break;
                }
            }
            //Mostrando mensaje de feriado(s) registrado(s)
            if (enviados) {
                JOptionPane.showMessageDialog(this, "Feriado(s) registrado(s) satisfactoriamente");
                MostrarFeriados();
                InhabilitarFeriado();
            }
        } else if (accionFeriado.equals("editar")) {
            dts.setId(Integer.parseInt(txt_id_feriado.getText()));
            if (func.editar(dts)) {
                JOptionPane.showMessageDialog(this, "Feriado editado satisfactoriamente");
                MostrarFeriados();
                InhabilitarFeriado();
            }
        }
    }//GEN-LAST:event_btn_guardar_feriadoActionPerformed

    private void btn_eliminar_feriadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_eliminar_feriadoActionPerformed
        if (!txt_id_feriado.getText().equals("")) {
            int confirmacion = JOptionPane.showConfirmDialog(this, "¿Estás seguro de eliminar el feriado?", "Confirmar", 2);
            if (confirmacion == 0) {
                //Lectura y obtencion de datos
                Feriado dts = new ConsulFeriado().obtener(Integer.parseInt(txt_id_feriado.getText()));
                //Envio de datos
                ConsulFeriado func = new ConsulFeriado();
                //Verificando conflictos
                if (new ConsulAgenda().conflictosFeriado(dts.getFecha())) {
                    JOptionPane.showMessageDialog(this, "Existen agendas que causan conflictos");
                    return;
                }
                if (func.eliminar(dts)) {
                    JOptionPane.showMessageDialog(this, "Feriado eliminado satisfactoriamente");
                    MostrarFeriados();
                    InhabilitarFeriado();
                }
            }
        }
    }//GEN-LAST:event_btn_eliminar_feriadoActionPerformed

