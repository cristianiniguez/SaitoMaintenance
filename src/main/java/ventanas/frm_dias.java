    private void btn_guardar_jornadaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardar_jornadaActionPerformed
        //Validacion de datos
        if (ych_anho.getValue() != dtc_fecha_inicial.getCalendar().get(Calendar.YEAR)
                || ych_anho.getValue() != dtc_fecha_final.getCalendar().get(Calendar.YEAR)) {
            JOptionPane.showMessageDialog(this, "Los años deben ser los mismos");
            return;
        }

        //Lectura de datos
        Jornada dts = new Jornada();
        dts.setAnho(ych_anho.getValue());
        dts.setFecha_inicial(Auxiliar.SQLDate(dtc_fecha_inicial.getCalendar()));
        dts.setFecha_final(Auxiliar.SQLDate(dtc_fecha_final.getCalendar()));

        //Verificando que la nueva jornada no cause conflictos
        if (new ConsulAgenda().conflictosJornada(dts)) {
            JOptionPane.showMessageDialog(this, "Hay conflictos con alguna(s) agenda(s)\nArreglar antes de guardar");
            return;
        }

        //Envio de datos
        ConsulJornada func = new ConsulJornada();
        if (func.guardar(dts)) {
            JOptionPane.showMessageDialog(this, "Jornada guardada satisfactoriamente");
            MostrarJornadas();
        }

    }//GEN-LAST:event_btn_guardar_jornadaActionPerformed

