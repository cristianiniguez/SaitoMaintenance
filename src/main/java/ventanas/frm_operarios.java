    private void btn_editarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editarActionPerformed
        //Validacion de datos
        if (txt_nombre.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "El operario no puede tener un nombre vacio");
            txt_nombre.requestFocus();
            return;
        }
        if (txt_ap_paterno.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "El operario no puede tener un apellido paterno vacio");
            txt_ap_paterno.requestFocus();
            return;
        }
        if (txt_ap_materno.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "El operario no puede tener un apellido materno vacio");
            txt_ap_materno.requestFocus();
            return;
        }

        //Lectura de datos
        Operario dts = new Operario();
        dts.setId(Integer.parseInt(txt_id_operario.getText()));
        dts.setNombre(txt_nombre.getText());
        dts.setAp_paterno(txt_ap_paterno.getText());
        dts.setAp_materno(txt_ap_materno.getText());
        dts.setEstado(cmb_estado.getSelectedItem().toString());

        //Verificando la no inhabilitaciÃ³n de una cuenta actualmente usada
        boolean cerrar = false;
        if (dts.getEstado().equals("Inhabilitado")) {
            int confirmacion = JOptionPane.showConfirmDialog(this, "EstÃ¡ seguro de inhabilitar su propia cuenta "
                    + "(se cerrarÃ¡ el programa):", "Confirmar", 2);
            if (confirmacion == 0) {
                cerrar = true;
            } else {
                return;
            }
        }

        //Envio de datos
        ConsulOperario func = new ConsulOperario();
        if (func.editarPublico(dts)) {
            JOptionPane.showMessageDialog(this, "Datos pÃºblicos del operario editados satisfactoriamente");
            //Actualiza los datos si el usuario cambiÃ³n sus propios datos
            if (dts.getId() == ConsulOperario.USUARIO.getId()) {
                new ConsulOperario().actualizarUsuario();
                pnl_sesionde.Mostrar();
            }
            //Cierra el programa si se inhabilito la cuenta del mismo usuario
            if (cerrar) {
                System.exit(0);
            }
            Mostrar();
            Inhabilitar();
        }
    }//GEN-LAST:event_btn_editarActionPerformed

    private void btn_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_eliminarActionPerformed
        if (!txt_id_operario.getText().equals("")) {
            //Lectura y obtencion de datos
            Operario dts = new ConsulOperario().obtener(Integer.parseInt(txt_id_operario.getText()));
            //El operario tiene ejecuciones realizadas
            if (new ConsulEjecucion().numEjecuciones(dts) > 0) {
                String msg = "Este operario no se puede eliminar\n"
                        + "Hay ejecuciones registradas de este operario\n"
                        + "Recuerde que puede inhabilitar la cuenta del operario";
                JOptionPane.showMessageDialog(this, msg);
                return;
            }
            //Confirmacion
            int confirmacion = JOptionPane.showConfirmDialog(this, "Â¿EstÃ¡s seguro de eliminar al operario?", "Confirmar", 2);
            if (confirmacion == 0) {
                //Envio de datos
                ConsulOperario func = new ConsulOperario();
                if (func.eliminar(dts)) {
                    JOptionPane.showMessageDialog(this, "Operario eliminada satisfactoriamente");
                    Mostrar();
                    Inhabilitar();
                }
            }
        }
    }//GEN-LAST:event_btn_eliminarActionPerformed

