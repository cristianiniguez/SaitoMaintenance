    //Muestra las maquina segun el area y la maquinaria
    private void MostrarMaquinas() {
        String area = cmb_areas.getSelectedItem().toString();
        String maquinaria = cmb_maquinarias.getSelectedItem().toString();
        tab_maquinas.setModel(new ConsulMaquina().mostrar(area, maquinaria));
        OcultarColumnasMaquina();
        lbl_num_registros_maquina.setText("Número de máquinas: " + ConsulMaquina.num_registros);
    }

    private void OcultarColumnasMaquina() {
        int[] columnas = {0, 3};
        for (int columna : columnas) {
            tab_maquinas.getColumnModel().getColumn(columna).setMaxWidth(0);
            tab_maquinas.getColumnModel().getColumn(columna).setMinWidth(0);
            tab_maquinas.getColumnModel().getColumn(columna).setPreferredWidth(0);
        }
    }

