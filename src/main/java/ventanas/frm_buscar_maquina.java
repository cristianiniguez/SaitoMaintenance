    private void tab_maquinasMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tab_maquinasMousePressed
        if (evt.getClickCount() == 2) {
            
            int fila = tab_maquinas.getSelectedRow();
            if (destino.equals("AG")) {
                frm_agenda.txt_id_maquina.setText(tab_maquinas.getValueAt(fila, 0).toString());
                frm_agenda.txt_codigo_maquina.setText(tab_maquinas.getValueAt(fila, 1).toString());
                frm_agenda.txt_id_mantenimiento.setText("");
                frm_agenda.txt_nombre_mantenimiento.setText("");
            }else if (destino.equals("EJ")) {
                frm_ejecucion.txt_id_maquina.setText(tab_maquinas.getValueAt(fila, 0).toString());
                frm_ejecucion.txt_codigo_maquina.setText(tab_maquinas.getValueAt(fila, 1).toString());
                frm_ejecucion.txt_id_mantenimiento.setText("");
                frm_ejecucion.txt_codigo_mantenimiento.setText("");
                frm_ejecucion.txt_nombre_mantenimiento.setText("");
                frm_ejecucion.txt_tipo_mantenimiento.setText("");
            }
            
            this.dispose();
        }
    }//GEN-LAST:event_tab_maquinasMousePressed

