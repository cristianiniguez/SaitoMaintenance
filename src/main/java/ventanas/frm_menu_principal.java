    //Muestra los mantenimientos de hoy
    public static void Mostrar() {
        String area = cmb_area.getSelectedItem().toString();
        String maquinaria = cmb_maquinaria.getSelectedItem().toString();
        Date fecha1 = Auxiliar.SQLDate(dtc_fecha1.getCalendar());
        Date fecha2 = Auxiliar.SQLDate(dtc_fecha2.getCalendar());
        tab_pendientes.setModel(new ConsulAgenda().mostrarPendientes(area, maquinaria, fecha1, fecha2));
        OcultarColumnas();
        lbl_num_registros.setText("NÃºmero de tareas pedientes: " + ConsulAgenda.num_registros);
    }

    private static void OcultarColumnas() {
        int[] columnas = {0, 1, 3, 4, 6};
        for (int columna : columnas) {
            tab_pendientes.getColumnModel().getColumn(columna).setMaxWidth(0);
            tab_pendientes.getColumnModel().getColumn(columna).setMinWidth(0);
            tab_pendientes.getColumnModel().getColumn(columna).setPreferredWidth(0);
        }
    }

