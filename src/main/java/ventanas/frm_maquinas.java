    private void formWindowClosed(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowClosed

        switch (ventana) {
            case "MP" -> { //Si lo abrió el gestionador de mantenimientos preventivos
                frm_mant_prev.ListaAreas();
                frm_mant_prev.cmb_areas.setSelectedIndex(0);
                frm_mant_prev.ListaMaquinarias();
                frm_mant_prev.cmb_maquinarias.setSelectedIndex(0);
                frm_mant_prev.MostrarMantenimientos();
                ventana = "";
            }
            case "MC" -> { //Si lo abrió el gestionador de mantenimientos correctivos
                frm_mant_corr.ListaAreas();
                frm_mant_corr.cmb_areas.setSelectedIndex(0);
                frm_mant_corr.ListaMaquinarias();
                frm_mant_corr.cmb_maquinarias.setSelectedIndex(0);
                frm_mant_corr.MostrarMantenimientos();
                ventana = "";
            }
            case "MN" -> { //Si lo abrió el manual
                frm_manual.ListaAreas();
                frm_manual.cmb_area.setSelectedIndex(0);
                frm_manual.ListaMaquinarias();
                frm_manual.cmb_maquinaria.setSelectedIndex(0);
                ventana = "";
            }
            default -> {
            }
        }
        ventana = "";
    }//GEN-LAST:event_formWindowClosed

