    private void btn_guardar_maquinariaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardar_maquinariaActionPerformed
        //Validacion de datos
        if (txt_id_area.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Se debe seleccionar el area para la maquinaria");
            btn_buscar_area.requestFocus();
            return;
        }
        if (txt_codigo_maquinaria.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Se debe ingresar un código para la maquinaria");
            txt_codigo_maquinaria.requestFocus();
            return;
        }
        if (txt_descripcion_maquinaria.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Se debe ingresar una descripción para la maquinaria");
            txt_descripcion_maquinaria.requestFocus();
            return;
        }

        //Lectura de datos
        Maquinaria dts = new Maquinaria();
        dts.setId_area(Integer.parseInt(txt_id_area.getText()));
        dts.setCodigo(txt_codigo_maquinaria.getText());
        dts.setDescripcion(txt_descripcion_maquinaria.getText());

        //Envio de datos
        ConsulMaquinaria func = new ConsulMaquinaria();
        if (accionMaquinaria.equals("guardar")) {
            if (func.insertar(dts)) {
                JOptionPane.showMessageDialog(this, "Maquinaria registrada satisfactoriamente");
                MostrarMaquinarias();
                InhabilitarMaquinaria();
            }
        } else if (accionMaquinaria.equals("editar")) {
            dts.setId(Integer.parseInt(txt_id_maquinaria.getText()));
            if (func.editar(dts)) {
                JOptionPane.showMessageDialog(this, "Maquinaria editada satisfactoriamente");
                MostrarMaquinarias();
                InhabilitarMaquinaria();
            }
        }
    }//GEN-LAST:event_btn_guardar_maquinariaActionPerformed

    private void btn_eliminar_maquinariaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_eliminar_maquinariaActionPerformed
        if (!txt_id_maquinaria.getText().equals("")) {
            //Lectura y obtencion de datos
            Maquinaria dts = new ConsulMaquinaria().obtener(Integer.parseInt(txt_id_maquinaria.getText()));
            //La maquinaria tiene maquinas?
            if (new ConsulMaquina().hayMaquina(dts)) {
                String msg = "Esta maquinaria no se puede eliminar\n"
                        + "Hay maquinas registradas de esta maquinaria";
                JOptionPane.showMessageDialog(this, msg);
                return;
            }
            //La maquinaria tiene mantenimientos?
            if (new ConsulMantenimiento().hayMantenimientos(dts)) {
                String msg = "Esta maquinaria no se puede eliminar\n"
                        + "Hay mantenimientos registrados de esta maquinaria";
                JOptionPane.showMessageDialog(this, msg);
                return;
            }
            //Confirmacion
            int confirmacion = JOptionPane.showConfirmDialog(this, "¿Estás seguro de eliminar la maquinaria?", "Confirmar", 2);
            if (confirmacion == 0) {
                //Envio de datos
                ConsulMaquinaria func = new ConsulMaquinaria();
                if (func.eliminar(dts)) {
                    JOptionPane.showMessageDialog(this, "Maquinaria eliminada satisfactoriamente");
                    MostrarMaquinarias();
                    InhabilitarMaquinaria();
                }
            }
        }
    }//GEN-LAST:event_btn_eliminar_maquinariaActionPerformed

