    private void tab_operariosMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tab_operariosMousePressed
        if (evt.getClickCount() == 2) {
            
            //Solo lo llama frm_ejecucion
            int fila = tab_operarios.getSelectedRow();
            frm_ejecucion.txt_id_operario.setText(tab_operarios.getValueAt(fila, 0).toString());
            String nombre = tab_operarios.getValueAt(fila, 1).toString();
            String ap_paterno = tab_operarios.getValueAt(fila, 2).toString();
            String ap_materno = tab_operarios.getValueAt(fila, 3).toString();
            frm_ejecucion.txt_nombre_operario.setText(nombre + " " + ap_paterno + " " + ap_materno);

            this.dispose();
        }
    }//GEN-LAST:event_tab_operariosMousePressed

