    private static void OcultarColumnas() {
        int[] columnas = {0, 4, 5};
        for (int columna : columnas) {
            tab_operarios.getColumnModel().getColumn(columna).setMaxWidth(0);
            tab_operarios.getColumnModel().getColumn(columna).setMinWidth(0);
            tab_operarios.getColumnModel().getColumn(columna).setPreferredWidth(0);
        }
    }

