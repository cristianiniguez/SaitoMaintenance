    private void formWindowClosed(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowClosed
        
        switch (ventana) {
            case "MAQ" -> {//Si lo abrió el gestionador de maquinas
                frm_maquinas.ListaAreas();
                frm_maquinas.cmb_areas.setSelectedIndex(0);
                frm_maquinas.MostrarMaquinarias();
            }
            case "MAN" -> {//Si lo abrió el manual
                frm_manual.ListaAreas();
                frm_manual.cmb_area.setSelectedIndex(0);
                frm_manual.ListaMaquinarias();
                frm_manual.cmb_maquinaria.setSelectedIndex(0);
            }
            default -> {
            }
        }
        ventana = "";
    }//GEN-LAST:event_formWindowClosed

