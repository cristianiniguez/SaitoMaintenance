    private void btn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarActionPerformed
        //Validacion de datos
        if (txt_nombre.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Se debe ingresar un nombre para el area");
            txt_nombre.requestFocus();
            return;
        }
        if (txt_descripcion.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Se debe ingresar una descripción para el area");
            txt_descripcion.requestFocus();
            return;
        }

        //Lectura de datos
        Area dts = new Area();
        dts.setNombre(txt_nombre.getText());
        dts.setDescripcion(txt_descripcion.getText());

        //Envio de datos
        ConsulArea func = new ConsulArea();
        if (accion.equals("guardar")) {
            if (func.insertar(dts)) {
                JOptionPane.showMessageDialog(this, "Area registrada satisfactoriamente");
                Mostrar();
                Inhabilitar();
            }
        } else if (accion.equals("editar")) {
            dts.setId(Integer.parseInt(txt_id_area.getText()));
            if (func.editar(dts)) {
                JOptionPane.showMessageDialog(this, "Area editada satisfactoriamente");
                Mostrar();
                Inhabilitar();
            }
        }

    }//GEN-LAST:event_btn_guardarActionPerformed

    private void btn_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_eliminarActionPerformed
        if (!txt_id_area.getText().equals("")) {
            //Lectura y obtencion de datos
            Area dts = new ConsulArea().obtener(Integer.parseInt(txt_id_area.getText()));
            //El area tiene maquinarias?
            if (new ConsulMaquinaria().hayMaquinaria(dts)) {
                String msg = "Esta area no se puede eliminar\n"
                        + "Hay maquinarias registradas de esta area";
                JOptionPane.showMessageDialog(this, msg);
                return;
            }
            //Confirmacion
            int confirmacion = JOptionPane.showConfirmDialog(this, "¿Estás seguro de eliminar la maquinaria?", "Confirmar", 2);
            if (confirmacion == 0) {
                //Envio de datos
                ConsulArea func = new ConsulArea();
                if (func.eliminar(dts)) {
                    JOptionPane.showMessageDialog(this, "Area eliminada satisfactoriamente");
                    Mostrar();
                    Inhabilitar();
                }
            }
        }
    }//GEN-LAST:event_btn_eliminarActionPerformed

